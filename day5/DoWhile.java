
class Dowhile {

    public static void main(String[] args) {
        System.out.println("DO WHILE LOOP");
        System.out.println("******************************");
        int i = 1;
        do {
            System.out.println("hitman");
            i++;
        } while (i <= 5);
        System.out.println("******************************");

        int b = 1;
        do {
            System.out.println(b);
            b++;
        } while (b <= 5);
        System.out.println("******************************");

        int a = 1;
        do {
            System.out.println(a + " * 5 = " + a * 5);
            a++;
        } while (a <= 5);
        System.out.println("******************************");

    }
}
