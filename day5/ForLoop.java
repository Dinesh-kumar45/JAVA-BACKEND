class ForLoop{
public static void main(String[] args) {
    System.out.println("=========================");
    System.out.println("For Loop");
    System.out.println("=========================");

    for(int i=1;i<=10;i++){
     System.out.println(i+" * 2 = "+i*2);
    }


     for(int a=10; a>=1;a--){
      System.out.println(a);
     }
    
}
}